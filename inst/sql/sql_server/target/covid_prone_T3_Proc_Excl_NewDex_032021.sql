) A on A.person_id = P.person_id  AND A.START_DATE >= P.OP_START_DATE AND A.START_DATE <= P.OP_END_DATE AND A.END_DATE >= DATEADD(day,-21,P.START_DATE) AND A.END_DATE <= DATEADD(day,7,P.START_DATE) ) cc 
